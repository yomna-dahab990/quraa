// دوال مساعدة
function getAzkarCategories() {
    global $azkar_categories;
    return $azkar_categories;
}

function getAzkarByCategory($category_id) {
    global $azkar;
    return array_filter($azkar, function($zekr) use ($category_id) {
        return $zekr['category_id'] == $category_id;
    });
}

function getAzkarBySubcategory($subcategory_id) {
    global $azkar;
    return array_filter($azkar, function($zekr) use ($subcategory_id) {
        return isset($zekr['subcategory_id']) && $zekr['subcategory_id'] == $subcategory_id;
    });
} 